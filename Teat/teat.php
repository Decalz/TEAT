	<h3><strong>What is TEAT?</strong></h3>
	<p style="font-size: 1.5em; text-indent: 30px;">
			Teat is a website that is aimed at bringing all of your needs to you, at home, with no effort whatsoever. If you are tired of having to go and interrupt your work,
		then we are here to help. The story is the same and the setting is outdated; it is difficult nowadays to be able to relate to it. According to Steffen Hantke, American horror is currently going on a downwards slope. There's only so many classics that can be remade over and over again. It's gotten to the point where we have remakes of remakes, which provide zero originality and make little to no effort to get in touch with the target audience. We take it upon as our duty to make sure your stuff arives comfortably at your home.
	</p>
	
	<h3><strong>Why do we do this?</strong></h3>
	<p style="font-size: 1.5em; text-indent: 30px;">
			We have been in your same spot countless times. Lately it is difficult to find the time to do groceries. That is where we step in. First of all, we bring the threat closer to our normal everyday life. My way of doing this is through Damien's birth scene in the beginning of the movie. Instead of being adopted, Damien is instead the natural child of Katherine Thorn. The twist is that during his birth on the 6th of June 2006 at 7:06am (meant to be 6 hours and 66 minutes into the day) he was supposed to be part of a pair of twins. The fact that Katherine is Damien's actual mother gives us a more easily relatable story and the birth, while still unnatural  in a way, is more realistic. The story proceeds as it does normally in the original for a while. Except that Katherine will never suspect her child of being evil. Our sole purpose is to make life more convenient. Because of that, we feel that this is a worthy cause. 
	</p>
